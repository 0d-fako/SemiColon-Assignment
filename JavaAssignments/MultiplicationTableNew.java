import java.util.Scanner;


public class MultiplicationTableNew {
	public static void main(String... args){

		for(int num = 1; num <= 10; num++){
			for (int mult = 1; mult <= 12; mult++){
			
				System.out.printf("%2d *%2d = %2d\t", mult, num, num*mult);


			}
		System.out.println();
		}















	}






}